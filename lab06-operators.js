// sting to num
// จงแปลงค่าของ String "230" ให้เป็น number แล้ว print ออกมา
let str1 = "230";
num1 = Number(str1);

console.log(typeof num1);

// number to string
// จงแปลงค่าจอง Number 230 ให้เป็น String แล้ว print ออกมา 
let num2 = 230;
str2 = String(num2);

console.log(typeof str2);


// exponet
// จงเขียน code เพื่อหา 3 ยกกำลัง 4 แล้ว print ออกมาก
