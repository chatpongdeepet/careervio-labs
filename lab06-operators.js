console.log(num3);

// compound assignment operators
// จงเขียน code เพื่อให้ ค่า x เท่ากับ 6 แล้วนำ x มาคูณ 20 โดยใช้ "การดำเนินการแบบย่อ"
let x = 6;
x *= 20;
console.log(x);